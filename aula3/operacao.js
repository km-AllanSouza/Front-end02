export {
    somar,
    subtrair,
    mult,
    div
}

function somar(param1, param2) {
    return param1 + param2
}

function subtrair(param1, param2) {
    return param1 - param2
}

function mult(param1, param2) {
    return param1 * param2
}

function div(param1, param2) {
    return param1 / param2
}